/**
 *
 * @param {number} number
 * @returns {number}
 */

const pow = (number) => {
  return Math.pow(number, 2);
};

export { pow };

/**
 * 
 * @param {number} number1 
 * @param {number} number2 
 * @returns {number}
 */
  
const sum = (number1, number2) => {
  return number1 + number2;
};

export { sum };
